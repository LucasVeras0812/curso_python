# Módulos padrão no python (import, from, as e *)
# https://docs.python.org/3/py-modindex.html
# inteiro - import nome_modulo
# Vantagens: voce tem o namespace do módulo
# Desvantagens: nomes grandes
import sys

# partes - from nome_modulo import objeto1, objeto2
# vantagens: nomes pequenos
# desvantagens sem o namespace do módulo

# alias 1 - import nome_modulo as apelido
# alias 2 - from nome_modulo import objeto as apelido
# Vantagens: voce pode reservar nomes para seu código
# Desvantagens: pode ficar fora do padrao da linguagem

# má pratica - from nome_modulo import *
# Vantagens: importa tudo de um módulo
# Desvantagens: importa tudo de um módulo

